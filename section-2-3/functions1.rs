// Introduction to Functions in Rust

// Functions are defined using the fn keyword
// They can take arguments, do operations and return values

fn define_function()
{
    println!("This is a function");
}

fn main()
{
    println!("Hello, world!");
    define_function();
    hello_world();
}

fn hello_world()
{
    println!("Hello, world!!!!!");
}


