// Conditions in Rust
// What is a condition?
// A condition is a statement that evaluates to true or false.
// Example: if a number is greater than 10, then print "Greater than 10".

fn main()
{
    let number = 6;

    if number > 10
    {
        println!("Greater than 10");
    }
    else 
    {
        println!("Less than 10");
    }
    
}

